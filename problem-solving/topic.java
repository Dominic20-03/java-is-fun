import java.util.*;

public class topic {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        char ch = input.next().charAt(0);

        if (ch == 'A') {
            System.out.println("YES");
        } else if (ch == 'B') {
            System.out.println("YES");
        } else if (ch == 'C') {
            System.out.println("YES");
        } else {
            System.out.println("NO");
        }
    }
}
